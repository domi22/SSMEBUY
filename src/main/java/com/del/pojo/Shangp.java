package com.del.pojo;

import java.io.Serializable;

public class Shangp implements Serializable {
	private Integer shangid;
	private String shangname;
	private Integer goodid;
	private String price;
	private String picpath;
	private Integer restcount;
	private String shangdic;
	public Shangp() {
		super();
	}
	public Integer getShangid() {
		return shangid;
	}
	public void setShangid(Integer shangid) {
		this.shangid = shangid;
	}
	public String getShangname() {
		return shangname;
	}
	public void setShangname(String shangname) {
		this.shangname = shangname;
	}
	public Integer getGoodid() {
		return goodid;
	}
	public void setGoodid(Integer goodid) {
		this.goodid = goodid;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getPicpath() {
		return picpath;
	}
	public void setPicpath(String picpath) {
		this.picpath = picpath;
	}
	public Integer getRestcount() {
		return restcount;
	}
	public void setRestcount(Integer restcount) {
		this.restcount = restcount;
	}
	public String getShangdic() {
		return shangdic;
	}
	public void setShangdic(String shangdic) {
		this.shangdic = shangdic;
	}
	
	
	
	
}
